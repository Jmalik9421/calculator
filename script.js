function add(num1, num2) {
    return num1 + num2;
}

function subtract(num1, num2) {
    return num2 - num1;
}

function multiply(num1, num2) {
    return num1 * num2;
}

function divide(num1, num2) {
    return num2 / num1;
}

// testing
console.log(add(5, 6))
console.log(subtract(5, 6))
console.log(multiply(5, 6))
console.log(divide(5, 6))